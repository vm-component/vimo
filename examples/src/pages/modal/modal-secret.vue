            当你使用我们的网站、在线服务和应用软件时，Apple 及我们的合作伙伴也会使用 Cookie 和其他技术来记录个人信息。在此等情形中，我们的目标是让你更便利、更具个性化地体验 Apple 产品与服务。例如，如果我们知道你的名字，便可以在你下一次访问 Apple Store 在线商店时向你致以欢迎。如果我们知道你所在的国家/地区和你使用的语言（如果你是教育工作者，知道你所在的学校），将有助于我们提供为你量身打造的、对你更有用的购物体验。如果我们知晓某人使用你的电脑或设备购买了某种产品或使用了某种服务，将有助于我们向你投放更符合你兴趣的广告和电子邮件。如果我们知道你的联系信息、硬件标识符以及有关你的电脑或设备的信息，我们就能更好地个性化你的操作系统、设置你的 iCloud 服务，并向你提供更好的客户服务。

            如果你希望禁用 Cookie，并且正在使用 Safari 网络浏览器，请转到 Safari 偏好设置，然后进入隐私面板管理你的偏好设置。在 Apple 移动设备上，转到“设置”，选择 Safari，然后向下滚动到“隐私与安全性”部分，轻点“阻止 Cookie”以管理你的偏好设置。对于其他浏览器，请联系你的供应商，了解如何禁用 Cookie。但请注意，禁用 Cookie 后，将无法使用 Apple 网站的某些功能。

            如同大多数互联网服务一样，我们也会自动收集某些信息，将其存储在日志文件中。这类信息包括 Internet 协议 (IP) 地址、浏览器类型和语言、互联网服务提供商 (ISP)、引荐/退出网站和应用软件、操作系统、日期/时间戳和点击流数据。

