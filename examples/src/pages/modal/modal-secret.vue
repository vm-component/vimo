            我们还会将个人信息用于审计、数据分析和研究等内部目的，以改进 Apple 的产品、服务和与客户之间的沟通。
            如果你参与抽奖、竞赛或类似推广活动，我们会将你提供的信息用于管理此类活动。
            非个人信息的收集和使用
            我们还会收集利用其本身无法与任何特定个人直接建立联系的数据。我们可出于任何目的而收集、使用、转让和披露非个人信息。下文是我们可能收集的非个人信息以及我们如何使用这些信息的一些示例。

            我们会收集诸如职业、语言、邮编、区号、唯一设备标识符、引荐 URL、位置以及用户在使用 Apple 产品时所处时区等信息，以便我们能更好地了解客户的行为，改进我们的产品、服务和广告宣传。
            我们会收集与客户在我们的网站上、iCloud 服务、我们的 iTunes Store、App Store、Mac App Store、适用于 Apple TV 的 App Store 和 iBooks Store 中的活动有关的信息，以及从我们其他产品和服务获得的信息。我们会将此类信息汇总，用于帮助我们向客户提供更有用的信息，了解客户对我们网站、产品和服务中的哪些部分最感兴趣。就本隐私政策而言，汇总数据被视为非个人信息。
